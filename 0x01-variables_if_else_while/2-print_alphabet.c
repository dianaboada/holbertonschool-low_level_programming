#include <stdio.h>
#include <stdlib.h>
#include <time.h>
/**
* main - Entry point
* Source code to print the last digit of the number stored in the variable n
* Return: Always 0 (Success)
*/
int main(void)
{
	int c;
	
	for (c = 'a'; c <= 'z'; c++)
	{
	putchar(c);
	}
	putchar('\n');
	return (0);

}

